    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       try{
            stmt = conn.createStatement();
            long Id = Long.parseLong(id.getText());
            String Name = name.getText();
            String Faculty = (String) faculty.getSelectedItem();
            String Department = (String) department.getSelectedItem();
            String PhoneNumber = phone.getText();
            String Mail = mail.getText();
            String City = city.getText();

            String dobText = dateOfBirth.getText();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd"); // Ensure the input is in this format: yyyy-MM-dd
            java.util.Date utilDate = sdf.parse(dobText);
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

            String sql = "UPDATE STUDENT SET stdName = '" + Name + "', dept = '" + Department + "', faculty = '" + Faculty + "', stdPhone = '" + PhoneNumber + "', stdMail = '" + Mail + "', dateOfBirth = '" + sqlDate + "', stdCity = '" + City + "' WHERE id = " + Id;

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data is successfully updated");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

