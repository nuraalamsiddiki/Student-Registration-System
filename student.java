    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            long Id = Long.parseLong(id.getText());
            
            String sql = "SELECT * FROM STUDENT WHERE id = '" + Id + "' ";
            rs = stmt.executeQuery(sql);
            if(rs.next()){
                name.setText(rs.getString("stdName"));
                department.setSelectedItem(rs.getString("dept"));
                faculty.setSelectedItem(rs.getString("faculty"));
                phone.setText(rs.getString("stdPhone"));
                mail.setText(rs.getString("stdMail"));
                city.setText(rs.getString("stdCity"));
                java.sql.Date sqlDate = rs.getDate("dateOfBirth");
                if (sqlDate != null) {
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    dateOfBirth.setText(sdf.format(sqlDate));
                }
                else {
                    // Show a message if the student with the given ID is not found
                    JOptionPane.showMessageDialog(null, "Student record not found.");
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

